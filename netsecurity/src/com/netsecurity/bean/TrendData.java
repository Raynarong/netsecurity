package com.netsecurity.bean;


public class TrendData {
	private String time;
	private Object data;
	public TrendData()
	{}
	
	public TrendData(String time, Object data)
	{
		super();
		this.time = time;
		this.data = data;
	}

	public String getTime()
	{
		return time;
	}

	public void setTime(String time)
	{
		this.time = time;
	}

	public Object getData()
	{
		return data;
	}

	public void setData(Object data)
	{
		this.data = data;
	}


	
	
	


	
}
